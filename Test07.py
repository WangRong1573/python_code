#   元组：声明后不可变,语法是 t =()，数组是[]
#   创建一个空元组
t = ()
print(type(t))

#   创建一个只有一个元素的元组，需要注意添加“,”
t2 = 1,
t3 = (1,)
print(type(t2))
print(type(t3))

#   创建一个元组
t4 = (1, 3, 5, 7, 4, 45, 78)
t5 = t4[:2]
print(t5)

# 元组的修改和删除，“修改”原理：将原元组拆开，组合成一个新的元组，替换原来的元组,原数组会被垃圾回收掉
temp = ('小黑鱼', '猪八戒', '沙和尚', '孙悟空')
temp = temp[:2] + ('唐僧',) + temp[2:]
print(temp)


